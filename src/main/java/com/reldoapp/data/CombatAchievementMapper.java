	/**
	 * Collect completed combat achievement tasks as a flat map of slug -> true.
	 * Matches the format stored in Reldo character files: { task_slug: true }.
	 * Must be called from the client thread.
	 */
	public static Map<String, Boolean> collectCombatAchievements(Client client)
	{
		Map<String, Boolean> result = new HashMap<>();
		for (Map.Entry<Integer, String> entry : TASK_MAP.entrySet())
		{
			int wikiId = entry.getKey();
			int varplayerIndex = wikiId / 32;
			int bit = wikiId % 32;
			int varplayerId = VARPLAYER_IDS[varplayerIndex];
			if (((client.getVarpValue(varplayerId) >> bit) & 1) == 1)
			{
				result.put(entry.getValue(), true);
			}
		}
		return result;
	}
