	private static Map<Integer, String> forLeague(String leagueId)
	{
		switch (leagueId)
		{
			case "raging_echoes":
				return RAGING_ECHOES;
			default:
				return null;
		}
	}

	/**
	 * Read all league task varbits for the given league and return completed task slugs.
	 * Returns null if the league is not supported. Must be called from the client thread.
	 */
	public static Map<String, Object> collectTasks(Client client, String leagueId)
	{
		Map<Integer, String> taskMap = forLeague(leagueId);
		if (taskMap == null) return null;

		Map<String, Object> result = new HashMap<>();
		for (Map.Entry<Integer, String> entry : taskMap.entrySet())
		{
			if (client.getVarbitValue(entry.getKey()) == 1)
			{
				result.put(entry.getValue(), true);
			}
		}
		return result;
	}
